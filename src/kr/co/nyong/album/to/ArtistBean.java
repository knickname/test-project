package kr.co.nyong.album.to;

public class ArtistBean {
	private String artistId;
	private String artistName;
	private String artistBirth;
	private String artistAgent;

	public String getArtistId() {
		return artistId;
	}
	public void setArtistId(String artistId) {
		this.artistId = artistId;
	}
	public String getArtistName() {
		return artistName;
	}
	public void setArtistName(String artistName) {
		this.artistName = artistName;
	}
	public String getArtistBirth() {
		return artistBirth;
	}
	public void setArtistBirth(String artistBirth) {
		this.artistBirth = artistBirth;
	}
	public String getArtistAgent() {
		return artistAgent;
	}
	public void setArtistAgent(String artistAgent) {
		this.artistAgent = artistAgent;
	}
	
}
